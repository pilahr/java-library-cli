package libraryZone.book;

import java.util.Comparator;

public class SortBooksByCounter implements Comparator<Book> {

    @Override
    public int compare(Book o1, Book o2) {
        if (o2.getCounter() == o1.getCounter()) {
            return (int) (o1.getId() - o2.getId());
        }
        return String.valueOf(o2.getCounter()).compareTo(String.valueOf(o1.getCounter()));
    }
}
